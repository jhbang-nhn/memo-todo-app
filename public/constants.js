// 공휴일 관련 유틸리티 함수
const HolidayUtils = {
    // 연도별 공휴일 가져오기
    getHolidaysForYear: (year) => {
        return HOLIDAYS_MULTI_YEAR[year] || {};
    },
    
    // 지원하는 연도 목록
    getSupportedYears: () => {
        return Object.keys(HOLIDAYS_MULTI_YEAR).map(year => parseInt(year));
    },
    
    // 새 연도 지원 여부 확인
    isYearSupported: (year) => {
        return HOLIDAYS_MULTI_YEAR.hasOwnProperty(year);
    }
};
